interface Project {
    _id: string; // Unique identifier for the project
    name: string; // Name of the project
    description: string; // Short description of the project
    picUrl: string; // URL for the project's image/logo
    framework: string; // Framework used in the project
    stateManagementSystem: string; // State management system used
    programmingLanguage: string; // Programming language used
    version: string; // Version of the framework/libraries
    videoUrl: string; // URL for a video demonstration or tutorial
}
